
class Traverse{
    public static void main(String[] args){
        int a[]=new int[8];//declaration and instantiation  
        a[0]=10;//initialization  
        a[1]=20;  
        a[2]=70;  
        a[3]=40;  
        
        //traversing array  
        for(int i=0;i<9;i++)//length is the property of array  
        System.out.println(a[i]);  
    }
 }
